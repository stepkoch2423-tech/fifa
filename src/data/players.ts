import type { Player } from "../types/football";
import { createPlayerAvatarDataUrl } from "../utils/playerHelpers";
import { CLUBS } from "./clubs";
import { PLAYER_PHOTOS } from "./playerPhotos";

type PlayerSeed = Omit<Player, "photoUrl"> & {
  photoUrl?: string;
};

const clubPaletteMap = new Map(
  CLUBS.map((club) => [
    club.id,
    {
      start: club.colors.start,
      end: club.colors.end,
    },
  ]),
);

function createPlayer(seed: PlayerSeed): Player {
  const palette = clubPaletteMap.get(seed.clubId) ?? {
    start: "#1e293b",
    end: "#0f172a",
  };

  return {
    ...seed,
    photoUrl:
      PLAYER_PHOTOS[seed.id] ??
      seed.photoUrl ??
      createPlayerAvatarDataUrl(seed.name, palette.start, palette.end),
  };
}

